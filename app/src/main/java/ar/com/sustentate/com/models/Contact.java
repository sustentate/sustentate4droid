public class Contact {
    private Long id;

    private String email;

    private String name;

    private String lastname;

    public Long getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getName() {
        return name;
    }

    public String getLastname() {
        return lastname;
    }
}
